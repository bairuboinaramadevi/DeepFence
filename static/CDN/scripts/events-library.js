async function insertTelemetry(
  eventSourceId,
  eventTypeId,
  eventDesc,
  eventData,
  ObjectId = null
) {
  console.log("insertTelemetry called")
  let paramsObj = {};
  paramsObj.functionName = "ManageTelemetry";
  paramsObj.moduleName = "Telemetry";
  paramsObj.api = "PlatformAPI"
  paramsObj.queryType = "insert";
  let telemetryObj = {};
  telemetryObj.UserID = userId;
  telemetryObj.EventSourceID = eventSourceId;
  telemetryObj.EventTypeID = eventTypeId;
  telemetryObj.EventDescription = eventDesc;
  telemetryObj.EventData = eventData;
  telemetryObj.ObjectID = ObjectId;
  paramsObj.TelemetryObj = telemetryObj;
  let url = "http://127.0.0.1:5000?dbParams=" + JSON.stringify(paramsObj);
  console.log("url", url);
  var data = await fetch(url);
  var res = data.json();
  return res;
}
 